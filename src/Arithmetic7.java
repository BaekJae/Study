
public class Arithmetic7 {

	public static void main(String[] args) {
		 
				boolean A = (3 > 10) & (10 > 3);
				boolean B = (3 > 10) && (10 > 3);
				boolean C = (10 > 3) | (3 > 10);
				boolean D = (10 > 3) || (3 > 10);
				System.out.println("A=" +A+ " B=" +B+ " C=" +C+ " D=" +D);
			
		}

	}


